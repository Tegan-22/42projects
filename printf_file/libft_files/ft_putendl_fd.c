#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		len;

	len = 0;
	if (!s)
		return ;
	while (s[len])
	{
		write(fd, &s[len], 1);
		len++;
	}
	write(fd, "\n", 1);
}
